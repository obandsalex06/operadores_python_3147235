'''
Condicional:
Expresion que, al ser evaluada,
da como resultado en valor verdader o falso.
Por lo tanto, en una condicional deben haber 
operadores relacionales o logicos 
'''

#Ejemplo de condicional

 
x = 5
y = 10
z = 5

resultado = (x == z + (8 / z)) and not ((y + 3) * (4 / (z + 1))) == z

print (resultado)